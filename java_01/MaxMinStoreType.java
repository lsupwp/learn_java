public class MaxMinStoreType {
    public static void main(String[] args) {
        System.out.println(Byte.MAX_VALUE);         // หาค่าที่เก็บได้สูงสุด
        System.out.println(Byte.MIN_VALUE);         // หาค่าที่เก็บได้ต่ำสุด

        System.out.println(Short.MAX_VALUE);         // หาค่าที่เก็บได้สูงสุด
        System.out.println(Short.MIN_VALUE);         // หาค่าที่เก็บได้ต่ำสุด

        System.out.println(Integer.MAX_VALUE);      // หาค่าที่เก็บได้สูงสุด
        System.out.println(Integer.MIN_VALUE);      // หาค่าที่เก็บได้ต่ำสุด

        System.out.println(Long.MAX_VALUE);         // หาค่าที่เก็บได้สูงสุด
        System.out.println(Long.MIN_VALUE);         // หาค่าที่เก็บได้ต่ำสุด

        System.out.println(Float.MAX_VALUE);        // หาค่าที่เก็บได้สูงสุด | 1.4E-45 (E คือเลขยำกำลัง)
        System.out.println(Float.MIN_VALUE);        // หาค่าที่เก็บได้ต่ำสุด

        System.out.println(Double.MAX_VALUE);        // หาค่าที่เก็บได้สูงสุด
        System.out.println(Double.MIN_VALUE);        // หาค่าที่เก็บได้ต่ำสุด
    }
}
